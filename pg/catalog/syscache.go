// QueryPgAttrdef returns pg_attrdef rows for a relation by OID.
//
// (pgddl helper — PG uses SearchSysCache with pg_attrdef)
func (c *Catalog) QueryPgAttrdef(relOID uint32) []PgAttrdefRow {
	rel := c.relationByOID[relOID]
	if rel == nil {
		return nil
	}

	var rows []PgAttrdefRow
	for _, col := range rel.Columns {
		// PG: identity columns do NOT have pg_attrdef entries;
		// identity sequence linkage is via pg_depend, not column defaults.
		// pg: src/backend/catalog/heap.c — identity handling
		if col.HasDefault && col.Identity == 0 {
			adBin := col.Default
			if col.DefaultAnalyzed != nil {
				rte := c.buildRelationRTE(rel)
				adBin = c.DeparseExpr(col.DefaultAnalyzed, []*RangeTableEntry{rte}, false)
			}
			rows = append(rows, PgAttrdefRow{
				AdRelID: relOID,
				AdNum:   col.AttNum,
				AdBin:   adBin,
			})
		}
	}
	return rows
}

// QueryPgTrigger returns pg_trigger rows for a relation by OID.
//
// (pgddl helper — PG uses SearchSysCache with pg_trigger)
func (c *Catalog) QueryPgTrigger(relOID uint32) []PgTriggerRow {
	var rows []PgTriggerRow
	for _, trig := range c.triggersByRel[relOID] {
		tgType := composeTriggerType(trig)
		tgEnabled := trig.Enabled
		if tgEnabled == 0 {
			tgEnabled = 'O' // default if not set
		}

		rows = append(rows, PgTriggerRow{
			OID:        trig.OID,
			TgRelID:    trig.RelOID,
			TgName:     trig.Name,
			TgFOID:     trig.FuncOID,
			TgType:     tgType,
			TgEnabled:  tgEnabled,
			TgAttr:     trig.Columns,
			TgQual:     trig.WhenExpr,
			TgOldTable: trig.OldTransitionName,
			TgNewTable: trig.NewTransitionName,
		})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].OID < rows[j].OID })
	return rows
}

// composeTriggerType builds the tgtype bitmask from trigger properties.
// pg: src/include/catalog/pg_trigger.h
func composeTriggerType(trig *Trigger) int16 {
	var tgtype int16
	if trig.ForEachRow {
		tgtype |= 1 // TRIGGER_TYPE_ROW
	}
	switch trig.Timing {
	case TriggerBefore:
		tgtype |= 2 // TRIGGER_TYPE_BEFORE
	case TriggerInsteadOf:
		tgtype |= 64 // TRIGGER_TYPE_INSTEAD
	default:
		// AFTER: bit not set (0 in the timing bits means AFTER)
	}
	if trig.Events&TriggerEventInsert != 0 {
		tgtype |= 4 // TRIGGER_TYPE_INSERT
	}
	if trig.Events&TriggerEventDelete != 0 {
		tgtype |= 8 // TRIGGER_TYPE_DELETE
	}
	if trig.Events&TriggerEventUpdate != 0 {
		tgtype |= 16 // TRIGGER_TYPE_UPDATE
	}
	if trig.Events&TriggerEventTruncate != 0 {
		tgtype |= 32 // TRIGGER_TYPE_TRUNCATE
	}
	return tgtype
}

// QueryPgProc returns pg_proc rows for user-created functions in a schema.
