// QueryPgNamespace returns all user-created schemas as pg_namespace rows.
//
// (pgddl helper — PG uses ScanPgRelation/SearchSysCache with pg_namespace)
func (c *Catalog) QueryPgNamespace() []PgNamespaceRow {
	var rows []PgNamespaceRow
	for _, s := range c.schemaByName {
		// Skip built-in schemas.
		if s.OID < FirstNormalObjectId && s.OID != PublicNamespace {
			continue
		}
		rows = append(rows, PgNamespaceRow{
			OID:     s.OID,
			NspName: s.Name,
		})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].OID < rows[j].OID })
	return rows
}

// QueryPgClass returns pg_class rows for all objects in the given schema.
//
// (pgddl helper — PG uses ScanPgRelation/SearchSysCache with pg_class)
func (c *Catalog) QueryPgClass(schemaName string) []PgClassRow {
	schema := c.schemaByName[schemaName]
	if schema == nil {
		return nil
	}

	var rows []PgClassRow

	// Relations (tables and views).
	for _, rel := range schema.Relations {
		persistence := rel.Persistence
		if persistence == 0 {
			persistence = 'p'
		}
		replicaIdentity := rel.ReplicaIdentity
		if replicaIdentity == 0 {
			switch rel.RelKind {
			case 'v', 'c':
				replicaIdentity = 'n' // views and composite types: nothing
			default:
				replicaIdentity = 'd' // tables and matviews: default
			}
		}
		row := PgClassRow{
			OID:                rel.OID,
			RelName:            rel.Name,
			RelNamespace:       schema.OID,
			RelType:            rel.RowTypeOID,
			RelKind:            rel.RelKind,
			RelNatts:           int16(len(rel.Columns)),
			RelPersistence:     persistence,
			ReplicaIdentity:    replicaIdentity,
			RelOfType:          rel.OfTypeOID,
			RelRowSecurity:     rel.RowSecurity,
			RelForceRowSecurity: rel.ForceRowSecurity,
		}

		// Count CHECK constraints.
		for _, con := range c.consByRel[rel.OID] {
			if con.Type == ConstraintCheck {
				row.RelChecks++
			}
		}

		// Has triggers?
		if len(c.triggersByRel[rel.OID]) > 0 {
			row.RelHasTriggers = true
		}

		// PG creates implicit RI triggers for FK constraints.
		// pg: src/backend/commands/tablecmds.c — CreateFKCheckTrigger, createForeignKeyActionTriggers
		if !row.RelHasTriggers {
			for _, con := range c.consByRel[rel.OID] {
				if con.Type == ConstraintFK {
					row.RelHasTriggers = true
					break
				}
			}
		}
		// Also check if this relation is referenced by FK constraints from other tables.
		if !row.RelHasTriggers {
			for _, cons := range c.consByRel {
				for _, con := range cons {
					if con.Type == ConstraintFK && con.FRelOID == rel.OID {
						row.RelHasTriggers = true
						break
					}
				}
				if row.RelHasTriggers {
					break
				}
			}
		}

		// Has indexes?
		if len(c.indexesByRel[rel.OID]) > 0 {
			row.RelHasIndex = true
		}

		rows = append(rows, row)
	}

	// Indexes (relkind='i').
	for _, idx := range schema.Indexes {
		row := PgClassRow{
			OID:             idx.OID,
			RelName:         idx.Name,
			RelNamespace:    schema.OID,
			RelKind:         'i',
			RelNatts:        int16(len(idx.Columns)),
			RelPersistence:  'p',
			ReplicaIdentity: 'n', // indexes always 'n' (nothing)
		}
		rows = append(rows, row)
	}

	// Sequences (relkind='S').
	for _, seq := range schema.Sequences {
		row := PgClassRow{
			OID:             seq.OID,
			RelName:         seq.Name,
			RelNamespace:    schema.OID,
			RelKind:         'S',
			RelNatts:        3, // PG sequences always have 3 columns (last_value, log_cnt, is_called)
			RelPersistence:  'p',
			ReplicaIdentity: 'n', // sequences always 'n' (nothing)
		}
		rows = append(rows, row)
	}

	sort.Slice(rows, func(i, j int) bool { return rows[i].OID < rows[j].OID })
	return rows
}

// QueryPgAttribute returns pg_attribute rows for a relation by OID.
//
// (pgddl helper — PG uses ScanPgRelation/SearchSysCache with pg_attribute)
func (c *Catalog) QueryPgAttribute(relOID uint32) []PgAttributeRow {
	rel := c.relationByOID[relOID]
	if rel == nil {
		return nil
	}

	rows := make([]PgAttributeRow, len(rel.Columns))
	for i, col := range rel.Columns {
		hasDef := col.HasDefault
		notNull := col.NotNull
		// PG: identity columns are NOT tracked via atthasdef; identity implies NOT NULL.
		// pg: src/backend/catalog/heap.c — identity handling
		if col.Identity != 0 {
			hasDef = false
			notNull = true
		}
		rows[i] = PgAttributeRow{
			AttRelID:     relOID,
			AttName:      col.Name,
			AttTypID:     col.TypeOID,
			AttNum:       col.AttNum,
			AttTypMod:    col.TypeMod,
			AttLen:       col.Len,
			AttByVal:     col.ByVal,
			AttAlign:     col.Align,
			AttStorage:   col.Storage,
			AttNotNull:   notNull,
			AttHasDef:    hasDef,
			AttIsDropped: false,
			AttCollation: col.Collation,
			AttGenerated: col.Generated,
			AttIdentity:  col.Identity,
			AttIsLocal:   col.IsLocal,
			AttInhCount:  col.InhCount,
		}
	}
	return rows
}

// QueryPgType returns pg_type rows for user-created types in a schema.
//
// (pgddl helper — PG uses SearchSysCache with pg_type)
func (c *Catalog) QueryPgType(schemaName string) []PgTypeRow {
	schema := c.schemaByName[schemaName]
	if schema == nil {
		return nil
	}

	var rows []PgTypeRow
	for _, bt := range c.typeByOID {
		if bt.Namespace != schema.OID {
			continue
		}
		// Only include user-created types (OID >= FirstNormalObjectId).
		if bt.OID < FirstNormalObjectId {
			continue
		}
		rows = append(rows, PgTypeRow{
			OID:          bt.OID,
			TypName:      bt.TypeName,
			TypNamespace: bt.Namespace,
			TypLen:       bt.Len,
			TypByVal:     bt.ByVal,
			TypType:      bt.Type,
			TypCategory:  bt.Category,
			TypIsDefined: bt.IsDefined,
			TypDelim:     bt.Delim,
			TypRelID:     bt.RelID,
			TypElem:      bt.Elem,
			TypArray:     bt.Array,
			TypBaseType:  bt.BaseType,
			TypTypMod:    bt.TypeMod,
			TypAlign:     bt.Align,
			TypStorage:   bt.Storage,
			TypNotNull:   bt.NotNull,
			TypCollation: bt.Collation,
		})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].OID < rows[j].OID })
	return rows
}

// QueryPgConstraint returns pg_constraint rows for a relation by OID.
//
// (pgddl helper — PG uses SearchSysCache with pg_constraint)
func (c *Catalog) QueryPgConstraint(relOID uint32) []PgConstraintRow {
	var rows []PgConstraintRow
	for _, con := range c.consByRel[relOID] {
		row := PgConstraintRow{
			OID:           con.OID,
			ConName:       con.Name,
			ConNamespace:  c.constraintNamespace(con),
			ConType:       byte(con.Type),
			ConRelID:      con.RelOID,
			ConKey:        con.Columns,
			ConIndexID:    con.IndexOID,
			ConValidated:  con.Validated,
			ConDeferrable: con.Deferrable,
			ConDeferred:   con.Deferred,
			ConParentID:   con.ConParentID,
			ConIsLocal:    con.ConIsLocal,
			ConInhCount:   con.ConInhCount,
			ConNoInherit:  con.ConNoInherit,
		}

		if con.Type == ConstraintFK {
			row.ConFRelID = con.FRelOID
			row.ConFKey = con.FColumns
			row.ConFUpdType = con.FKUpdAction
			row.ConFDelType = con.FKDelAction
			row.ConFMatchType = con.FKMatchType
			row.PFEqOp = con.PFEqOp
			row.PPEqOp = con.PPEqOp
			row.FFEqOp = con.FFEqOp
			row.FKDelSetCols = con.FKDelSetCols
		}

		if con.Type == ConstraintCheck {
			// pg: pg_get_constraintdef_worker — CHECK (%s)
			// Non-pretty deparse wraps ops/boolexprs in parens, so
			// CHECK (%s) already gets the right paren nesting.
			if con.CheckAnalyzed != nil {
				rel := c.relationByOID[con.RelOID]
				if rel != nil {
					rte := c.buildRelationRTE(rel)
					exprText := c.DeparseExpr(con.CheckAnalyzed, []*RangeTableEntry{rte}, false)
					row.ConExpr = "CHECK (" + exprText + ")"
				} else {
					row.ConExpr = "CHECK ((" + con.CheckExpr + "))"
				}
			} else {
				row.ConExpr = "CHECK ((" + con.CheckExpr + "))"
			}
		}

		rows = append(rows, row)
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].OID < rows[j].OID })
	return rows
}

// constraintNamespace returns the namespace OID for a constraint.
func (c *Catalog) constraintNamespace(con *Constraint) uint32 {
	if con.Namespace != 0 {
		return con.Namespace
	}
	// Derive from the owning relation.
	if rel := c.relationByOID[con.RelOID]; rel != nil && rel.Schema != nil {
		return rel.Schema.OID
	}
	return 0
}

// QueryPgIndex returns pg_index rows for a relation by OID.
//
// (pgddl helper — PG uses SearchSysCache with pg_index)
func (c *Catalog) QueryPgIndex(relOID uint32) []PgIndexRow {
	var rows []PgIndexRow
	for _, idx := range c.indexesByRel[relOID] {
		nKeyAtts := int16(idx.NKeyColumns)
		if nKeyAtts == 0 {
			nKeyAtts = int16(len(idx.Columns))
		}
		// Build indexprs: PG's pg_get_expr concatenates expression texts.
		indExprs := ""
		if len(idx.Exprs) > 0 {
			indExprs = strings.Join(idx.Exprs, ", ")
		}
		row := PgIndexRow{
			IndexRelID:     idx.OID,
			IndRelID:       idx.RelOID,
			IndNatts:       int16(len(idx.Columns)),
			IndNKeyAtts:    nKeyAtts,
			IndIsUnique:    idx.IsUnique,
			IndIsPrimary:   idx.IsPrimary,
			IndIsClustered: idx.IsClustered,
			IndIsReplIdent: idx.IsReplicaIdent,
			IndKey:         idx.Columns,
			IndOption:      idx.IndOption,
			IndExprs:       indExprs,
			IndPred:        idx.WhereClause,
		}
		rows = append(rows, row)
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].IndexRelID < rows[j].IndexRelID })
	return rows
}

