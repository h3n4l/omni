// PgPolicyRow mirrors pg_policy.
type PgPolicyRow struct {
	OID        uint32
	PolName    string
	PolRelID   uint32
	PolCmd     string // "all", "select", "insert", "update", "delete"
	Permissive bool
	Roles      []string
	PolQual    string // USING expression
	PolCheck   string // WITH CHECK expression
}

// QueryPgPolicy returns pg_policy rows for the given relation OID.
// If relOID is 0, returns all policies.
//
// pg: src/include/catalog/pg_policy.h
func (c *Catalog) QueryPgPolicy(relOID uint32) []PgPolicyRow {
	var rows []PgPolicyRow
	var policies []*Policy
	if relOID != 0 {
		policies = c.policiesByRel[relOID]
	} else {
		for _, p := range c.policies {
			policies = append(policies, p)
		}
	}
	for _, p := range policies {
		polQual := p.UsingExpr
		polCheck := p.CheckExpr
		if p.UsingAnalyzed != nil || p.CheckAnalyzed != nil {
			rel := c.relationByOID[p.RelOID]
			if rel != nil {
				rte := c.buildRelationRTE(rel)
				rtable := []*RangeTableEntry{rte}
				if p.UsingAnalyzed != nil {
					polQual = c.DeparseExpr(p.UsingAnalyzed, rtable, false)
				}
				if p.CheckAnalyzed != nil {
					polCheck = c.DeparseExpr(p.CheckAnalyzed, rtable, false)
				}
			}
		}
		rows = append(rows, PgPolicyRow{
			OID:        p.OID,
			PolName:    p.Name,
			PolRelID:   p.RelOID,
			PolCmd:     p.CmdType,
			Permissive: p.Permissive,
			Roles:      p.Roles,
			PolQual:    polQual,
			PolCheck:   polCheck,
		})
	}
	sort.Slice(rows, func(i, j int) bool {
		return rows[i].OID < rows[j].OID
	})
	return rows
}

// PgDomainConstraintRow mirrors a domain CHECK constraint as visible through pg_constraint.
type PgDomainConstraintRow struct {
	DomainName   string
	ConName      string
	ConDef       string // CHECK ((expr)) form
	ConValidated bool
}

// GetDomainConstraintByOID returns the name of a domain constraint by its OID,
// or empty string if not found.
//
// (pgddl helper — PG uses SearchSysCache1(CONSTROID, ...))
func (c *Catalog) GetDomainConstraintByOID(oid uint32) string {
	for _, dt := range c.domainTypes {
		for _, dc := range dt.Constraints {
			if dc.OID == oid {
				return dc.Name
			}
		}
	}
	return ""
}

// QueryPgDomainConstraints returns domain CHECK constraints for a given schema.
//
// (pgddl helper — PG uses pg_constraint with contypid != 0)
func (c *Catalog) QueryPgDomainConstraints(schemaName string) []PgDomainConstraintRow {
	schema := c.schemaByName[schemaName]
	if schema == nil {
		return nil
	}

	var rows []PgDomainConstraintRow
	for oid, dt := range c.domainTypes {
		bt := c.typeByOID[oid]
		if bt == nil || bt.Namespace != schema.OID {
			continue
		}
		// pg: PG17+ creates a contype='n' constraint row for domain NOT NULL.
		// pg: src/backend/commands/typecmds.c — DefineDomain (NOT NULL handling)
		if dt.NotNull {
			rows = append(rows, PgDomainConstraintRow{
				DomainName:   bt.TypeName,
				ConName:      bt.TypeName + "_not_null",
				ConDef:       "NOT NULL",
				ConValidated: true,
			})
		}
		for _, dc := range dt.Constraints {
			var conDef string
			if dc.CheckAnalyzed != nil {
				// pg: pg_get_constraintdef(oid, true) — pretty with PRETTYFLAG_PAREN
				// CoerceToDomainValueExpr deparses as "VALUE" directly.
				exprText := c.DeparseExpr(dc.CheckAnalyzed, nil, true)
				conDef = "CHECK (" + exprText + ")"
			} else {
				conDef = "CHECK ((" + dc.CheckExpr + "))"
			}
			rows = append(rows, PgDomainConstraintRow{
				DomainName:   bt.TypeName,
				ConName:      dc.Name,
				ConDef:       conDef,
				ConValidated: dc.ConValidated,
			})
		}
	}
	sort.Slice(rows, func(i, j int) bool {
		if rows[i].DomainName != rows[j].DomainName {
			return rows[i].DomainName < rows[j].DomainName
		}
		return rows[i].ConName < rows[j].ConName
	})
	return rows
}

// objTypeToClassOID maps internal object type chars to pg_class OIDs.
func objTypeToClassOID(objType byte) uint32 {
	switch objType {
	case 'r': // relation (table, view)
		return RelationRelationID
	case 'i': // index
		return RelationRelationID // indexes are in pg_class too
	case 'c': // constraint
		return ConstraintRelationID
	case 't': // type
		return TypeRelationID
	case 'f': // function/procedure
		return ProcedureRelationID
	case 'g': // trigger
		return TriggerRelationID
	case 'n': // namespace/schema
		return NamespaceRelationID
	case 's': // sequence
		return RelationRelationID // sequences are in pg_class too
	default:
		return 0
	}
}
